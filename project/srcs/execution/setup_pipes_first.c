#include "../../incs/minishell.h"

void	setup_pipes_first(int **pp)
{
	if (pp[0])
	{
		close(pp[0][0]);
		dup2(pp[0][1], 1);
		close(pp[0][1]);
		pp++;
		while (*pp)
		{
			close((*pp)[0]);
			close((*pp)[1]);
			pp++;
		}
	}
}
